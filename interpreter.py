x,y,z=input("Expression: ").split(" ")

if y== "+":
    Answer = float(x) + float(z)
    print(Answer)
elif y== "-":
    Answer = float(x) - float(z)
    print(Answer)
elif y== "*":
    Answer = float(x) * float(z)
    print(Answer)
elif y== "/":
    Answer = float(x) / float(z)
    print(Answer)

    
